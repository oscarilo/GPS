    private void btnGuardarCambiosActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGuardarCambiosActionPerformed
        // TODO add your handling code here:
        int registros = tablaPermisos.getRowCount();//obtenemos el numero de registro que tienen que actualizar
        for(int i = 0; i < registros; i++){

            manager_permisos.actualizarPermisos(Principal.usuario,tablaPermisos.getValueAt(i, 0).toString(), (boolean)tablaPermisos.getValueAt(i, 1), 
            (boolean)tablaPermisos.getValueAt(i, 2),(boolean)tablaPermisos.getValueAt(i, 3), (boolean) tablaPermisos.getValueAt(i, 4));

        }//Actualizamos
        
        JOptionPane.showMessageDialog(null, "Los cambios han sido actualizados exitosamente.");
        this.dispose();
        
    }//GEN-LAST:event_btnGuardarCambiosActionPerformed

